/*
 * Codigo ejemplo para explicar los tipos de datos en C++
 */
#include <iostream>

using namespace std;

int main()
{
    //TIPO DE DATO PRMITIVO:
    //Esto es un entero (int)
    cout << 1337 << endl;
    //Esto es un nÃºmero decimal.
    cout << 3.1416f << endl;
    //Esto es un caracter
    cout << '&' << endl;
    //Esto es un boolean.
    cout << true << endl;
    
    //TIPO DE DATO COMPLEJO:
    //Esto es una cadena de caracteres
    cout << "En clase aprendo mucho" << endl;

    return 0;
}
